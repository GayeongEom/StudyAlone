package java01_Variables;

public class Variables_02 {
	
	public static void main(String[] args) {
		
		int data; //int타입의 변수공간을 선언(Declaration) / int형 데이터를 저장할 수 있는 메모리공간(변수)이 생성됨
		
		
		data = 17; //변수 공간에 값을 대입(Assignment) /17이라는 값을 data라는 변수에 저장
		
		data = 88; //data변수에 88 저장 -> 기존에 있던 값 17 지워짐
		
		System.out.println( data ); //data변수에 저장된 값(88)을 불러와서 출력
		
	}

}
