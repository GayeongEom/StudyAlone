package java02_operator;

public class Unary_01 {
	public static void main(String[] args) {
		
		//단항 연산자 - 부호, Sign
		//	+ -
		//	. 참조연산자
		//	! 논리부정연산자
		//	(type) 형변환 연산자
		
		//-----------------------
		
		int num1 = -7; //음수
		int num2 = +7; //양수
		
		System.out.println( num1 );
		
		System.out.println( -num1 );
		System.out.println( -(-7) ); //변수값 활용
		System.out.println( 7 ); //부호 변환
		
		
		
		
	}

}
